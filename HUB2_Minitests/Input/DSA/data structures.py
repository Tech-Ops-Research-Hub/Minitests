#Arrays and #Lists

#Arrays 
#Fixed size, can't change 
#Same type of elements (e.g all numbers)
#Example: int arr [3]={1,2,3};

#Lists
#Can grow or shrink
#Can mix types (e.g numbers and strings)
#Example: my_list = [1, "two", 3.0]

#Key Differences 
#Arrays: Fixed, fast, same type 
#Lists: Dynamic, flexible, mixed types

