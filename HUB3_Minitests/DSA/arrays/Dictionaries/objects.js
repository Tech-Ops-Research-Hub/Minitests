//A javascript object is similar to a python dictionary, storing key-value pairs where keys are typically strings(or symbols) and values can be any type (numbers, strings, arrays, functions, other objects etc)
//Syntax; Defined using curly braces {} with key-value pairs separated by commas

//Examples; 

let myobj = {name: "Bob", age: 30, scores: [85, 90, 88]}; // Creating an object with multiple key-value pairs
console.log(myobj.name);
myobj.scores.push(100);
console.log(myobj.scores); 
myobj.grade="B"; 
console.log(myobj.grade); // Adding a new grade to the object