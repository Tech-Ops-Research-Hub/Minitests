#Arrays are ordered collctions of elements of the same data type stored in contiguous memory locations
#Arrays are mutable, meaning their elements can be changed after creation

#Key features of arrays:
#Fixed Size: The size of an array is defined at the time of creation and cannot be changed
#Homogeneous Elements: All elements in an array must be of the same data type (e.g all integers, all floats, etc)
#Performance: Arrays provide efficient access to elements using indices, making them faster for certain operations compared to other data structures like lists


#Key differences between arrays and lists:
#Lists can store elements of different data types, while arrays require all elements to be of the same type
#Lists are more flexible and can grow or shrink in size, while arrays have a fixed size

#In short; Arrays are fixed-size:fast;same type elements while lists are dynamic-mix data types:slower;different type elements

#List: A list is a built-in Python data structure that can hold an ordered collection of items, which can be of different data types. Lists are mutable, meaning you can change their content after creation. Lists are defined using square brackets [].